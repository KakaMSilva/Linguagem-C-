#include <stdio.h>
#include <stdlib.h>

//Void, significa vazio que ira retorna nada
void main(){

       //Definindo uma variavel int, inteira
       int a = 5, b;

       //Imprimindo a variavel "a" que contem valor 5
       printf("%d ", a);

       //concatenando
       printf("\no valor da var a e: %d", a);

       //Mudando valor de "a"
       a=15;

       //Concatenando
       printf("\no valor da var a e: %d", a);

       //Lendo valores
       scanf("%d", &b);

       //Concatenando
       printf("\no valor da var b e: %d", b);

       //Usam \n pra pular linha
       printf("\n");

       //Imprimindo
       printf("Uhuu ");

       //Pausando
       system("Pause ");
}
